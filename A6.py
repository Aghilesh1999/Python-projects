#string slicing

s = "lets go on a long trip for a time"
print(s)

print(s[1:5])

print(s[1:])

print(s[1:-6])

print(s[1:-3])

print(s[1:-1])

print(s[0:1000])

print(s[0:8])
print(s[::])

print(s[::-1]) #reverse
print(s[1:-2])

print(s*2)

print(s[0:12:3]) #remove 3 letters
print(len(s))
